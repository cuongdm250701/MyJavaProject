package midteam;

public class book {
	int id;
	String name;
	int price;
	
	public void setID (int id) {
		this.id = id;
	}
	
	public int getID() {
		return this.id;
	}
	
	public void setName (String name) {
		this.name = name;
	}
	
	public String getName() {
		return this.name;
	}
	
	public void setPrice (int price) {
		this.price = price;
	}
	
	public int getPrice() {
		return this.price;
	}
	
}
